'use strict';

const e = React.createElement;

class TaskList extends React.Component {
  constructor(props) {
    super(props);
    this.state = { liked: false };
  }

  render() {
    if (this.state.liked) {
      return 'You liked this.';
    }

    return e(
      'button',
      { onClick: () => this.setState({ liked: true }) },
      'Like'
    );
  }
}


ReactDOM.render(
  <TaskList />,
  document.getElementById('reactList')
);